var PaymentViewModel = function (source, options) {
	var model = this;

	model.Options = $.extend({
		AccountingDetailsUrl: null
	}, options);

	// поля ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	model.Payment = ko.mapping.fromJS(source);


	// вычисляемое/подписки ///////////////////////////////////////////////////////////////////////////////////////////////////


	// функции ////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	model.AccountingDetailsUrl = function (id)
	{
		return model.Options.AccountingDetailsUrl + "/" + ko.unwrap(id);
	}


	// инициализация //////////////////////////////////////////////////////////////////////////////////////////////////////////
	

}

$(function () {
	ko.applyBindings(new PaymentViewModel(modelData, {
		AccountingDetailsUrl: app.urls.AccountingDetails
	}), document.getElementById("ko-root"));
});